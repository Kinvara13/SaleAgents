#!/usr/bin/env bash
# -----------------------------------------------------------------------------
# 一键停止脚本：安全关闭所有通过 start.sh 启动的进程
# -----------------------------------------------------------------------------

# 获取脚本绝对路径
PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PID_FILE="${PROJECT_ROOT}/.run.pid"

echo "========================================================="
echo "🛑 正在停止 SaleAgents V2 服务..."
echo "========================================================="

# 检查进程锁文件
if [ ! -f "${PID_FILE}" ]; then
    echo "⚠️ 未找到进程锁文件 (${PID_FILE})。"
    echo "可能服务未通过 start.sh 启动，或者已经异常退出。"
    echo "如果端口被占用，请尝试使用 \`lsof -i :8000\` 和 \`lsof -i :5173\` 手动查杀进程。"
    exit 0
fi

# 逐行读取进程ID并杀掉
while read -r PID; do
    if [ -n "$PID" ] && ps -p "$PID" > /dev/null; then
        echo "🔪 正在结束进程 $PID..."
        kill "$PID" || true
        # 等待进程退出
        sleep 1
        # 如果还在运行，强制结束
        if ps -p "$PID" > /dev/null; then
            echo "⚠️ 进程 $PID 未响应，正在强制结束..."
            kill -9 "$PID" || true
        fi
    else
        echo "ℹ️ 进程 $PID 已经结束或不存在。"
    fi
done < "${PID_FILE}"

# 清理进程锁文件
rm -f "${PID_FILE}"

echo "========================================================="
echo "✅ 所有相关服务已成功停止！"
echo "========================================================="
